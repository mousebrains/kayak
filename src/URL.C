  int dehex(const int c) {
    if ((c >= '0') && (c <= '9'))
      return (c - '0') & 0x0f;
    if ((c >= 'a') && (c <= 'f'))
      return ((c - 'a') + 10) & 0x0f;
    if ((c >= 'A') && (c <= 'F'))
      return ((c - 'A') + 10) & 0x0f;
    return 0;
  }

